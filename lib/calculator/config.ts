/** Тариф направления: карта «вес (кг) → цена (₽)». Между точками — линейная интерполяция. */
export type TariffCurve = Record<string, number>

export interface Direction {
	/** латинский код (id) направления */
	code: string
	/** название города (RU) */
	name: string
	/** ориентировочный срок доставки, календарных дней */
	days: number
	/** кривая «вес → цена», склад-склад (магистраль, как «Автоперевозка» у ПЭК) */
	curve: TariffCurve
}

