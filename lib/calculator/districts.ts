// Переопределение по названию города — для записей, где поле region не несёт
// округа (напр. Норильск с region="Россия").
const CITY_DISTRICT: Record<string, FederalDistrict> = {
	норильск: 'siberia',
}

/**
 * Определить федеральный округ по региону и/или названию населённого пункта.
 * Сначала пробуем по региону (надёжнее), затем — по названию города.
 */
export function resolveDistrict(name: string, region: string): FederalDistrict | null {
	const r = normalize(region || '')
	if (r && REGION_DISTRICT[r]) return REGION_DISTRICT[r]
	const n = normalize(name || '')
	if (n && CITY_DISTRICT[n]) return CITY_DISTRICT[n]
	if (n && REGION_DISTRICT[n]) return REGION_DISTRICT[n]
	return null
}

